<?php
/**
 * TVfrog - First-time setup wizard
 * Creates config.php with your admin password.
 */

$configPath = __DIR__ . '/config.php';

if (file_exists($configPath)) {
    if ($_SERVER['REQUEST_METHOD'] === 'POST') {
        header('Content-Type: application/json');
        echo json_encode(['error' => 'Already configured.']);
        exit;
    }
    echo "<!DOCTYPE html><html><body style='font-family:sans-serif;text-align:center;padding:60px;'>
        <h2>TVfrog is already configured</h2>
        <p>Delete admin/api/config.php to reconfigure.</p>
        <a href='../../'>Go to TVfrog</a>
    </body></html>";
    exit;
}

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    header('Content-Type: application/json');
    $password = trim($_POST['password'] ?? '');

    if (strlen($password) < 6) {
        echo json_encode(['error' => 'Password must be at least 6 characters.']);
        exit;
    }

    $hash = password_hash($password, PASSWORD_BCRYPT);

    $config = "<?php\n";
    $config .= "// TVfrog Configuration - Generated by setup wizard\n";
    $config .= "// Date: " . date('Y-m-d H:i:s') . "\n\n";
    $config .= "\$adminPasswordHash = " . var_export($hash, true) . ";\n";

    if (file_put_contents($configPath, $config) === false) {
        echo json_encode(['error' => 'Could not write config.php. Check folder permissions.']);
        exit;
    }

    // Create default data file
    $dataPath = __DIR__ . '/../../data/app_data.json';
    if (!file_exists($dataPath)) {
        @mkdir(dirname($dataPath), 0755, true);
        $defaultData = [
            'userLists' => ['Favourites' => [], 'Watchlist' => [], 'Seen' => [], 'To Finish' => []],
            'followedShows' => [],
            'showData' => (object)[]
        ];
        file_put_contents($dataPath, json_encode($defaultData, JSON_PRETTY_PRINT));
    }

    echo json_encode(['success' => true]);
    exit;
}
