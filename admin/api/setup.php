    <script>
    document.getElementById('setupForm').addEventListener('submit', async (e) => {
        e.preventDefault();
        const btn = document.getElementById('submitBtn');
        const msg = document.getElementById('msg');
        btn.disabled = true; btn.textContent = 'Setting up...';
        msg.className = 'msg'; msg.style.display = 'none';

        const fd = new FormData(e.target);
        try {
            const r = await fetch('setup.php', { method: 'POST', body: fd });
            const d = await r.json();
            if (d.success) {
                msg.className = 'msg ok'; msg.textContent = 'Setup complete! Redirecting...'; msg.style.display = 'block';
                setTimeout(() => window.location.href = '../../', 1500);
            } else {
                msg.className = 'msg err'; msg.textContent = d.error; msg.style.display = 'block';
                btn.disabled = false; btn.textContent = 'Set Up TVfrog';
            }
        } catch (err) {
            msg.className = 'msg err'; msg.textContent = 'Connection error.'; msg.style.display = 'block';
            btn.disabled = false; btn.textContent = 'Set Up TVfrog';
        }
    });
